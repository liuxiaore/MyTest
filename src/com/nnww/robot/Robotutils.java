package com.nnww.robot;

public class Robotutils {

    public static int getKeyCode(int num) {
        int i = 0;
        switch (num) {
            case 0:
                i = 48;
                break;
            case 1:
                i = 49;
                break;
            case 2:
                i = 50;
                break;
            case 3:
                i = 51;
                break;
            case 4:
                i = 52;
                break;
            case 5:
                i = 53;
                break;
            case 6:
                i = 54;
                break;
            case 7:
                i = 55;
                break;
            case 8:
                i = 56;
                break;
            case 9:
                i = 57;
                break;

        }
        return i;
    }
}
